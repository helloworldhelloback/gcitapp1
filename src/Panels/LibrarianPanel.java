    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = this.jTable1.getSelectedRow();
        TableModel model = this.jTable1.getModel();
        if(getCurrentTable().equals(TableEnum.BOOKCOPIES)){
            txt_BookCopiesNum.setText(model.getValueAt(index, 3).toString());
            setNumBookCopies(Integer.valueOf(txt_BookCopiesNum.getText()));            
            setBookId(Integer.valueOf(model.getValueAt(index, 0).toString()));
        }
        if(getCurrentTable().equals(TableEnum.LIBRARYINFO)){
            txt_branchName.setText(model.getValueAt(index, 1).toString());
            txt_branchAddress.setText(model.getValueAt(index, 2).toString());
            setBranchName(txt_branchName.getText());
            setBranchAddress(txt_branchAddress.getText());
        }
    }//GEN-LAST:event_jTable1MouseClicked


