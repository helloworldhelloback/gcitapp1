    public void run(){
        setListeners();
        setBookCopiesTable();
    }
    
    public void setBookCopiesTable(){
        this.jTable1.setModel(getBookCopiesTable().getModel());
    }
    public void setLibraryInfoTable(){
        this.jTable1.setModel(getLibraryInfoTable().getModel());
    }
    public JTable getBookCopiesTable(){
        TablesColumns columns= new TablesColumns();
        return columns.selectBookCopiesInBranch(getDbQueries(),getBranchName());
    }
    
    public JTable getLibraryInfoTable(){
        TablesColumns columns= new TablesColumns();
        return columns.selectLibrarianBranch(getDbQueries(),getBranchName());
    }
    public void reset(){
        setBookCopiesTable();
        cleanUp();
    }   
    public void cleanUp(){
        txt_BookCopiesNum.setText("");
        txt_branchAddress.setText("");
        txt_branchName.setText("");
    }
    public void setListeners(){    
        this.btn_TableBooks.addActionListener(this);
        this.btn_TableLibrary.addActionListener(this);
        this.btn_UpdateBookCopies.addActionListener(this);
        this.btn_UpdateLibraryInfo.addActionListener(this);
    }
    
    @Override
    public void actionPerformed(ActionEvent e) {
        if(e.getSource() == btn_TableBooks){  
            setCurrentTable(TableEnum.BOOKCOPIES);
            cleanUp();
            setBookCopiesTable();
        }
        if(e.getSource() == btn_TableLibrary){
            setCurrentTable(TableEnum.LIBRARYINFO);
            cleanUp();
            setLibraryInfoTable();
        }  
        if(e.getSource() == btn_UpdateBookCopies){
            setNumBookCopies(Integer.valueOf(txt_BookCopiesNum.getText()));
            System.out.println(getBookId() + ", " +getBranchId() +", "+ getNumBookCopies());
            getDbQueries().setBookCopiesNum(getBookId(), getBranchId(), getNumBookCopies());
            setBookCopiesTable();
        }  
        if(e.getSource() == btn_UpdateLibraryInfo){
            System.out.println("branchID= "+getBranchId()+" branchAddress= "+getBranchName());
            setBranchName(txt_branchName.getText());
            setBranchAddress(txt_branchAddress.getText());
            getDbQueries().setBranchInfo(getBranchId(), getBranchName(), branchAddress);
            setLibraryInfoTable();
        }  
        
    }
    
